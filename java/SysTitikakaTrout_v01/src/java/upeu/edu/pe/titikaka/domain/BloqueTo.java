package upeu.edu.pe.titikaka.domain;

/**
 *
 * @author USUARIO
 */
public class BloqueTo {
    
    private String idBloque;
    private String nombre;
    private String totalpeces;
    private String totalpeso;
    private String estado;

    public String getIdBloque() {
        return idBloque;
    }

    public void setIdBloque(String idBloque) {
        this.idBloque = idBloque;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTotalpeces() {
        return totalpeces;
    }

    public void setTotalpeces(String totalpeces) {
        this.totalpeces = totalpeces;
    }

    public String getTotalpeso() {
        return totalpeso;
    }

    public void setTotalpeso(String totalpeso) {
        this.totalpeso = totalpeso;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
 
    

    
}    
